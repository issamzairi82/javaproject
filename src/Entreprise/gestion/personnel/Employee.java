    private void buttonrechercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonrechercherActionPerformed
        try{
        dt.setRowCount(0); // vider la liste des stagiaires
        if(jRadioButton1.isSelected()){ // radioboutton 'mat'
            Rs = St.executeQuery("Select * From employee WHERE cin = "+textrecherchere.getText());
        }else{
            Rs = St.executeQuery("Select * From employee WHERE nom = '"+textrecherchere.getText()+"'");
        }
        while(Rs.next()){
              cin = Rs.getInt("cin");
            nom = Rs.getString("nom");
            prenom = Rs.getString("prenom");
            dateNaiss = Rs.getDate("date_naiss");
            tel = Rs.getInt("tel");
            grade = Rs.getString("grade");
            salaire = Rs.getInt("salaire");
            fonction = Rs.getString("fonction");
            abs = Rs.getInt("absence");
            Object[] stg = {cin,nom,prenom,dateNaiss,tel,grade,salaire,fonction,abs};
            dt.addRow(stg);
           
        }
        if (dt.getRowCount() == 0){
            JOptionPane.showMessageDialog(null, "Aucun Stagiaire");
            AfficherEmployee(); // afficehr tout les employeés
        }else{
           int i = 0;
            DeplacerEmployee(i);
        }
        
    }catch(Exception e){
        JOptionPane.showMessageDialog(null, "Erreur btn rechercher stg !\n"+e.getMessage());
    }
    }//GEN-LAST:event_buttonrechercherActionPerformed

