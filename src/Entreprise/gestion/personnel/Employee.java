    private void AfficherEmployee(){
    try{
        dt.setRowCount(0); // vider la list des stagiaires.
        Rs = St.executeQuery("Select * From employee");
        
        while(Rs.next()){
            cin = Rs.getInt("cin");
            nom = Rs.getString("nom");
            prenom = Rs.getString("prenom");
            dateNaiss = Rs.getDate("date_naiss");
            tel = Rs.getInt("tel");
            grade = Rs.getString("grade");
            salaire = Rs.getInt("salaire");
            fonction = Rs.getString("fonction");
            abs = Rs.getInt("absence");
            Object[] stg = {cin,nom,prenom,dateNaiss,tel,grade,salaire,fonction,abs};
            dt.addRow(stg);
        }
    }catch(Exception e){
        JOptionPane.showMessageDialog(null, "Erreur d'affichage\n"+e.getMessage());
    }
}
    
    
    
