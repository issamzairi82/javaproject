    private void buttonrechercherActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonrechercherActionPerformed
        try{
        dt.setRowCount(0); // vider la liste des stagiaires
        if(jRadioButton1.isSelected()){ // radioboutton 'mat'
            Rs = St.executeQuery("Select * From stagiaire WHERE cin = "+textrechercher.getText());
        }else{
            Rs = St.executeQuery("Select * From stagiaire WHERE nom = '"+textrechercher.getText()+"'");
        }
        while(Rs.next()){
              cin = Rs.getInt("cin");
            nom = Rs.getString("nom");
            prenom = Rs.getString("prenom");
            dateNaiss = Rs.getDate("date_naiss");
            tel = Rs.getInt("tel");
            ts = Rs.getString("titre_de_stage");
            ds = Rs.getDate("debut_de_stage");
            fs = Rs.getDate("fin_de_stage");
            univ = Rs.getString("universite");
            Object[] stg = {cin,nom,prenom,dateNaiss,tel,ts,ds,fs,univ};
            dt.addRow(stg);
           
        }
        if (dt.getRowCount() == 0){
            JOptionPane.showMessageDialog(null, "Aucun Stagiaire");
            AfficherStagiaire(); // afficehr tout les stgs
        }else{
           int i = 0;
            DeplacerStagiaire(i);
        }
        
    }catch(Exception e){
        JOptionPane.showMessageDialog(null, "Erreur btn rechercher stg !\n"+e.getMessage());
    }
    }//GEN-LAST:event_buttonrechercherActionPerformed

